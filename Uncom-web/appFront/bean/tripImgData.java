package bean;

public class tripImgData {
	private Integer id;
	private String url;
	private String tripAttractionId;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getTripAttractionId() {
		return tripAttractionId;
	}
	public void setTripAttractionId(String tripAttractionId) {
		this.tripAttractionId = tripAttractionId;
	}
	
	
	
}
