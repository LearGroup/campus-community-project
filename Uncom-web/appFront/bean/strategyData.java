package bean;

public class strategyData {

	private String id;
	private String theStrategy;
	private String transport;
	private String tripAttractionId;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getTheStrategy() {
		return theStrategy;
	}
	public void setTheStrategy(String theStrategy) {
		this.theStrategy = theStrategy;
	}
	public String getTransport() {
		return transport;
	}
	public void setTransport(String transport) {
		this.transport = transport;
	}
	public String getTripAttractionId() {
		return tripAttractionId;
	}
	public void setTripAttractionId(String tripAttractionId) {
		this.tripAttractionId = tripAttractionId;
	}
	
	
}
