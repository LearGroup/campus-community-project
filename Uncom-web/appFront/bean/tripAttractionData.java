package bean;

public class tripAttractionData {
	private String id;
	private String tripAttractionName;
	private Float longitude;
	private Float  latitude;
	private String position;
	private String sectionTime;
	private String peopleNumber;
	private Integer thePriceOfTicket;
	private Integer minimumConsumption;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getTripAttractionName() {
		return tripAttractionName;
	}
	public void setTripAttractionName(String tripAttractionName) {
		this.tripAttractionName = tripAttractionName;
	}
	public Float getLongitude() {
		return longitude;
	}
	public void setLongitude(Float longitude) {
		this.longitude = longitude;
	}
	public Float getLatitude() {
		return latitude;
	}
	public void setLatitude(Float latitude) {
		this.latitude = latitude;
	}
	public String getPosition() {
		return position;
	}
	public void setPosition(String position) {
		this.position = position;
	}
	public String getSectionTime() {
		return sectionTime;
	}
	public void setSectionTime(String sectionTime) {
		this.sectionTime = sectionTime;
	}
	public String getPeopleNumber() {
		return peopleNumber;
	}
	public void setPeopleNumber(String peopleNumber) {
		this.peopleNumber = peopleNumber;
	}
	public Integer getThePriceOfTicket() {
		return thePriceOfTicket;
	}
	public void setThePriceOfTicket(Integer thePriceOfTicket) {
		this.thePriceOfTicket = thePriceOfTicket;
	}
	public Integer getMinimumConsumption() {
		return minimumConsumption;
	}
	public void setMinimumConsumption(Integer minimumConsumption) {
		this.minimumConsumption = minimumConsumption;
	}
	
	
	
	
	
}
